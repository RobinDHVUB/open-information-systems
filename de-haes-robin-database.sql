/*
Data Sources
------------

Non-flight specific data, e.g. passenger names or account numbers, were mostly automatically generated by Mockaroo.
Most flight-related data such as airports and airplanes have been randomly selected from the open source tool OpenFlights.
As inspiration for flight options that are not provided in Openflights, the website of Brussels Airlines has been consulted.
Some information from the aforementioned sources has been modified and some information has been invented by ourselves
to ensure we would be able to clearly demonstrate certain functionalities of our system. 

In summary, the following data sources have been used:

- https://www.mockaroo.com
- https://openflights.org/data.html
- https://www.brusselsairlines.com


Remarks
-------

Even though we used camelcasing in our ERD, we used a naming convention that uses only lowercase characters for our database.
The reason for this is that pgAdmin4 puts uppercase names between double quotes, which made it necessary to constantly use
double quotes in our queries as well. This would have made our queries less readable, which is why we used no uppercase characters
in our table and column names.

Some columns currently have sizes that might be a bit too large, e.g. the country has 64 characters but our demo data actually 
has country ISO codes which are much shorter than 64.
Even though we currently store countries as ISO code, this is just a demo database. The schema does not prevent anybody from
adding actual country names in that column (which can be longer than a simple ISO code).
So generally we chose to use larger sizes than might be required to be on the safe side and prevent any possible future problems.

More constraints were possible, but we chose to not restrict our database any further than what was explicitly specified in the ERD.
This means we only added the following constraints:
- Primary Keys 
- Foreign Keys
- NOT NULL constraints on all primary keys
- NOT NULL constraints on foreign keys if they aren't optional (i.e. no constraint is added in case of a 0..1 cardinality)
Other constraints would have been possible, e.g. forcing an email address or creation date to be NOT NULL, 
but all additional constraints are expected to be implemented as business logic in a layer above our actual database. 
They seem to simply be out of scope for this part of the project.
*/


